M1=MIN
M2=MIN
M3=MIN
FOR(I=0;I<N;I++){
    IF(A[I]>M1){
        M3=M2
        M2=M1
        M1=A[I]
    }
    ELSE IF(A[I]>M2 && A[I]<M1){
        M3=M2
        M2=A[I]
    }
    ELSE IF(A[I]>M3 && A[I]<M2){
        M3=A[I]
    }
}

LXI H,F000
MOV B,M
INX H
MOV A,M
