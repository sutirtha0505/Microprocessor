//SQUARE ROOT OF A NUMBER
F000: 16
F001: SQRT(F000)

LXI H,F000H     //LOAD HL REGISTER PAIR WITH THE ADDRESS
MOV A,M         //ACCUMULATOR GETS THE VALUE FROM THE MEMORY
MVI C,00        //REGISTER C=0
MVI B,01        //REGISTER B=01
L2:SUB B           //SUBTRACT B FROM ACCUMULATOR
JC L1
INR B
INR B
INR C
JMP L2
L1:MOV A,C      //CALCULATED SQRT
STA F001H       //PRINTS THE SQRT
HLT
